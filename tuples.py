days = ("monday","tuesday","wednesday","thursday")
print(days)
print(type(days))

#accessing items in a tuple
print(days[3])
print(days[0])
print(days[-1])

#finding number of items in a tuple
print(len(days))

#index method=returing the index of a specified value
print(days.index("tuesday"))

#count=returns the number of occurances of an item in the tuple
print(days.count("thursday"))